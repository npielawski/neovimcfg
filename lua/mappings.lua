-- Mappings
vim.keymap.set('', '<C-C>', '<Esc>', {noremap=true})
vim.keymap.set('i', '<C-C>', '<Esc>', {noremap=true})

vim.keymap.set('', '<Up>', '<C-W><Up>', {noremap=true})
vim.keymap.set('', '<Down>', '<C-W><Down>', {noremap=true})
vim.keymap.set('', '<Left>', '<C-W><Left>', {noremap=true})
vim.keymap.set('', '<Right>', '<C-W><Right>', {noremap=true})
-- Terminal can be exited with <Esc>
vim.keymap.set('t', '<Esc>', '<C-\\><C-n>', {noremap=true})
